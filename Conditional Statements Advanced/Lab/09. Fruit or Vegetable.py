product = str(input())

if product == "banana" or product == "apple" or product == "kiwi" or product == "cherry" or product == "lemon" or product == "grapes":
    print("fruit")

if product == "tomato" or product == "cucumber" or product == "pepper" or product == "carrot":
    print("vegetable")

if product != "banana" and product != "apple" and product != "kiwi" and product != "cherry" and product != "lemon" and product != "grapes" and product != "tomato" and product != "cucumber" and product != "pepper" and product != "carrot":
    print("unknown")
